// ***********************************************************
// BEGIN OF CLASS
// ***********************************************************
class chip extends tpl {

function __construct() {
	parent::__construct();
	$this->load("user/chip.tpl");
}

// ***********************************************************
// methods
// ***********************************************************
public function setData($pic, $name, $tel, $email = "-") {
	$this->set("pic", $pic);
	$this->set("alt", $name);
	$this->set("name", $name);
	$this->set("tel", $tel);
	$this->set("email", $email);
}

// ***********************************************************
} // END OF CLASS
