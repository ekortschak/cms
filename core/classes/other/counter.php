class counter {
	private $dat = array();

function __construct() {}

// ***********************************************************
// methods
// ***********************************************************
public function count($key) {
	if (! isset($this->dat[$key])) $this->dat[$key] = 0;
	$this->dat[$key]++;
}

public function getData() {
	return VEC::sort($this->dat, "arsort");
}

public function show() {
	DBG::vector($this->dat);
}

// ***********************************************************
} // END OF CLASS
