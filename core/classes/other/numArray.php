class numArray {
	private $arr = array();

function __construct() {}

// ***********************************************************
// methods
// ***********************************************************
public function log($key) {
	if (! isset($this->arr[$key])) {
		$this->arr[$key] = 0;
	}
	$cnt = intval($this->arr[$key]);
	$cnt++;

	$this->arr[$key] = $cnt;
}

public function get() {
	arsort($this->arr);

	return $this->arr;
}

// ***********************************************************
} // END OF CLASS
