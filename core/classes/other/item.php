class item extends objects {

function __construct($name, $value = "") {
	$nam = trim($name);
	$val = trim($value);

	$this->set("index", $nam);
	$this->set("head",  $nam);
	$this->set("value", $val);
}

// ***********************************************************
} // END OF CLASS
