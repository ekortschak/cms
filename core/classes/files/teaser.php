class teaser extends tpl {

function __construct() {
	parent::__construct();
	$this->read("design/templates/modules/teaser.tpl");
}

// ***********************************************************
// methods
// ***********************************************************
public function setPics($dir) {
	$arr = FSO::files($dir); if (! $arr) return;
	$fls = implode("', '", $arr);

	$this->set("pix", "'$fls'");
	$this->set("pic", $arr[0]);
}

// ***********************************************************
} // END OF CLASS
